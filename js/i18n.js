/* ── helpers ── */
function translateNode(node) {
  const text = node.nodeValue ? node.nodeValue.trim() : '';
  if (!text) return;
  
  if (translations[text]) {
    node.nodeValue = node.nodeValue.replace(text, translations[text]);
  } else {
    let newText = node.nodeValue;
    let modified = false;
    
    const partials = {
      " ago": " مضت",
      "m ago": " دقائق مضت",
      "h ago": " ساعات مضت",
      " units remaining": " وحدات متبقية",
      " units left": " وحدات متبقية",
      " requested": " تم طلبه",
      " assigned": " مُسند",
      " pending": " معلق",
      "Total ": "إجمالي ",
      "Select ": "اختر ",
      " tasks": " مهام",
      " task": " مهمة",
      " active tasks": " مهام نشطة",
      " active task": " مهمة نشطة",
      "In Progress": "قيد التنفيذ",
      "Completed": "مكتمل",
      "Waiting Parts": "في انتظار قطع الغيار",
      "Unassigned": "غير مُسند",
      "Pending Approval": "بانتظار الموافقة",
      "High": "عالية",
      "Medium": "متوسطة",
      "Low": "منخفضة"
    };
    
    for (const en in partials) {
      if (newText.includes(en)) {
        newText = newText.split(en).join(partials[en]);
        modified = true;
      }
    }
    
    if (modified) {
      node.nodeValue = newText;
    }
  }
}

function translateElement(root) {
  // Text nodes
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
  let node;
  while ((node = walker.nextNode())) translateNode(node);

  // Placeholders and Values
  root.querySelectorAll && root.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
    const ph = el.getAttribute('placeholder');
    if (ph && translations[ph]) el.setAttribute('placeholder', translations[ph]);
  });
  
  root.querySelectorAll && root.querySelectorAll('input[type="button"], input[type="submit"]').forEach(el => {
    const val = el.getAttribute('value');
    if (val && translations[val]) el.setAttribute('value', translations[val]);
  });

  // title attributes
  root.querySelectorAll && root.querySelectorAll('[title]').forEach(el => {
    const t = el.getAttribute('title');
    if (t && translations[t]) el.setAttribute('title', translations[t]);
  });
}

/* ── main apply ── */
function applyTranslations() {
  if (currentLang === 'en') return;

  document.documentElement.dir = 'rtl';
  document.documentElement.lang = 'ar';

  // Inject Tajawal Arabic font once
  if (!document.getElementById('tajawal-font')) {
    const link = document.createElement('link');
    link.id = 'tajawal-font';
    link.rel = 'stylesheet';
    link.href = 'https://fonts.googleapis.com/css2?family=Tajawal:wght@400;500;700;800&display=swap';
    document.head.appendChild(link);
  }
  document.body.style.fontFamily = "'Tajawal', 'Inter', sans-serif";

  translateElement(document.body);
}

/* expose globally so page scripts can call window.applyTranslations() after render */
window.applyTranslations = applyTranslations;

/* ── MutationObserver: translate newly injected DOM nodes automatically ── */
function startObserver() {
  if (currentLang === 'en') return;
  const observer = new MutationObserver(mutations => {
    mutations.forEach(m => {
      m.addedNodes.forEach(n => {
        if (n.nodeType === Node.TEXT_NODE) {
          translateNode(n);
        } else if (n.nodeType === Node.ELEMENT_NODE) {
          translateElement(n);
        }
      });
    });
  });
  observer.observe(document.body, { childList: true, subtree: true });
}

/* ── Toggle ── */
function toggleLanguage() {
  const newLang = currentLang === 'en' ? 'ar' : 'en';
  localStorage.setItem('mems_lang', newLang);
  window.location.reload();
}

